	VkShaderModule VulkanPipeline::CreateShaderModule(const std::vector<char>& code){
		VkShaderModuleCreateInfo createInfo{};
		createInfo.sType = VK_STRUCTURE_TYPE_SHADER_MODULE_CREATE_INFO;
		createInfo.codeSize = code.size();
		createInfo.pCode = reinterpret_cast<const uint32_t*>(code.data());

		VkShaderModule shaderModule;
		if (vkCreateShaderModule(vDevice->device, &createInfo, nullptr, &shaderModule) != VK_SUCCESS){
			throw std::runtime_error("Failed to create shader module.");
		}

		return shaderModule;
	}

	void VulkanPipeline::CreateAllGraphicPipelines() {
		for (auto& shader : root->assetManager.loadedShaders){
			CreateGraphicsPipeline(shader.first);
		}
	}
	
	void VulkanPipeline::Cleanup() {
		for (auto& shader : root->assetManager.loadedShaders){
			vkDestroyPipeline(vDevice->device, shader.second.graphicsPipeline, nullptr);
			vkDestroyPipelineLayout(vDevice->device, shader.second.pipelineLayout, nullptr);
		}
	}
